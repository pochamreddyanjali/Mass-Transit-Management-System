function addUpRoutes(routes) {
  const updatedRoutes = [];
  const specialSkips = new Set(["3D", "101"]);

  for (const route of routes) {
    const { routeId, stations } = route;

    // Handle original route
    const direction = routeId.endsWith("U") ? "Up" : "Down";
    updatedRoutes.push({ ...route, direction });

    // Skip specific routeIds and non-Down routes
    if (!routeId.endsWith("D") || specialSkips.has(routeId)) continue;

    // Create reverse route (Up)
    const baseId = routeId.slice(0, -1);
    const upRouteId = baseId + "U";

    updatedRoutes.push({
      routeId: upRouteId,
      stations: [...stations].reverse(),
      direction: "Up",
    });
  }

  // Special case: Add 201 as reverse of 101
  const route101 = routes.find((r) => r.routeId === "101");
  if (route101) {
    updatedRoutes.push({
      routeId: "201",
      stations: [...route101.stations].reverse(),
      direction: "Up",
    });
  }

  return updatedRoutes;
}

